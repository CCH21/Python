# 思路如下：
# 设原来有(5x+1)个桃子
# 第1只猴子拿走了x个桃子，还有4x=5y+1个桃子
# 第2只猴子拿走了y个桃子，还有4y=5z+1个桃子
# 第3只猴子拿走了z个桃子，还有4z=5m+1个桃子
# 第4只猴子拿走了m个桃子，还有4m=5n+1个桃子
# 第5只猴子拿走了n个桃子
# 从n=1开始枚举，直到x,y,z,m均为整数
n = 1
while n:
    m = (5 * n + 1) / 4
    z = (5 * m + 1) / 4
    y = (5 * z + 1) / 4
    x = (5 * y + 1) / 4
    if int(x) == x and int(y) == y and int(z) == z and int(m) == m:
        break
    else:
        n += 1
print(int(5 * x + 1))
